var RukunTetangga = "RT 1";

switch (RukunTetangga) {
  case "RT 1":
    console.log("Blok Rumah A-F Adalah RT 1.");
    break;
  case "RT 2":
    console.log("Blok Rumah G-L Adalah RT 2.");
    break;
  case "RT 3":
    console.log("Blok Rumah M-R Adalah RT 3.");
    break;
  case "RT 4":
    console.log("Blok Rumah S-Z Adalah RT 4.");
    break;
  default:
    console.log("Hanya Ada 4 RT di Perumahan Ini!");
}
